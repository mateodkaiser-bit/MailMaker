// SECTION 3: tiptapToJsx — TipTap JSON → React Email .jsx source code
// ═══════════════════════════════════════════════════════════════════════════════

function tiptapToJsx(doc, componentName, variables, subject) {
  const nodes = doc.content || [];

  // Collect {{ params.X }} variable names used in this template
  const usedVarNames = collectParamVars(nodes);

  // Build props map: shortName → fallback
  const propsMap = new Map();
  for (const name of usedVarNames) {
    propsMap.set(name, '');
  }
  for (const v of variables || []) {
    const shortName = v.name.replace(/^params\./, '');
    if (propsMap.has(shortName)) propsMap.set(shortName, v.fallback || '');
  }

  // Determine which React Email components are needed
  const usedComponents = collectUsedComponents(nodes);
  const allComponents = ['Html', 'Head', 'Body', 'Container', ...usedComponents];
  const uniqueComponents = [...new Set(allComponents)];

  // Generate JSX for content blocks
  const blocksJsx = nodes
    .map(node => nodeToJsx(node, 3))
    .filter(Boolean)
    .join('\n');

  // Build props parameter
  let propsParam = '';
  if (propsMap.size > 0) {
    const entries = [...propsMap.entries()]
      .map(([k, v]) => `  ${k} = ${JSON.stringify(v)},`)
      .join('\n');
    propsParam = `{\n${entries}\n} = {}`;
  }

  const importLine = `import { ${uniqueComponents.join(', ')} } from '@react-email/components';`;
  const subjectExport = subject ? `\nexport const subject = ${JSON.stringify(subject)};\n` : '';
  const fnSignature = propsParam
    ? `export default function ${componentName}(${propsParam}) {`
    : `export default function ${componentName}() {`;

  return [
    importLine,
    subjectExport,
    fnSignature,
    `  return (`,
    `    <Html>`,
    `      <Head />`,
    `      <Body style={{ backgroundColor: '#ffffff', margin: 0, padding: 0 }}>`,
    `        <Container style={{ maxWidth: '600px', margin: '0 auto' }}>`,
    blocksJsx || `          {/* empty */}`,
    `        </Container>`,
    `      </Body>`,
    `    </Html>`,
    `  );`,
    `}`,
    '',
  ].join('\n');
}

// Collect all {{ params.X }} variable short-names from TipTap content
function collectParamVars(nodes) {
  const vars = new Set();
  const RE = /\{\{\s*params\.(\w+)\s*\}\}/g;

  function scan(node) {
    if (node.type === 'text' && node.text) {
      for (const m of node.text.matchAll(RE)) vars.add(m[1]);
    }
    if (node.attrs) {
      for (const val of Object.values(node.attrs)) {
        if (typeof val === 'string') {
          for (const m of val.matchAll(RE)) vars.add(m[1]);
        }
      }
    }
    if (node.marks) {
      for (const mark of node.marks) {
        if (mark.attrs) {
          for (const val of Object.values(mark.attrs)) {
            if (typeof val === 'string') {
              for (const m of val.matchAll(RE)) vars.add(m[1]);
            }
          }
        }
      }
    }
    if (node.content) for (const child of node.content) scan(child);
  }

  for (const node of nodes) scan(node);
  return [...vars];
}

// Determine which React Email components are needed
function collectUsedComponents(nodes) {
  const comps = new Set();
  function scan(node) {
    switch (node.type) {
      case 'paragraph':
      case 'heading':
      case 'bulletList':
      case 'orderedList':
        comps.add('Text'); break;
      case 'blockImage':
        comps.add('Img');
        if (node.attrs?.href) comps.add('Link');
        break;
      case 'blockButton': comps.add('Button'); break;
      case 'blockDivider': comps.add('Hr'); break;
      case 'blockSpacer': comps.add('Section'); break;
      case 'blockColumns': comps.add('Section'); comps.add('Column'); break;
      case 'blockSocialIcons': comps.add('Row'); comps.add('Link'); comps.add('Img'); break;
    }
    if (node.content) for (const child of node.content) scan(child);
  }
  for (const node of nodes) scan(node);
  return [...comps];
}

// ─── Node → JSX string ────────────────────────────────────────────────────────

function nodeToJsx(node, indentLevel) {
  const pad = '  '.repeat(indentLevel);
  switch (node.type) {
    case 'paragraph':    return paragraphToJsx(node, pad);
    case 'heading':      return headingToJsx(node, pad);
    case 'bulletList':
    case 'orderedList':  return bulletListToJsx(node, pad);
    case 'blockImage':   return imageToJsx(node, pad);
    case 'blockButton':  return buttonToJsx(node, pad);
    case 'blockDivider': return dividerToJsx(node, pad);
    case 'blockSpacer':  return spacerToJsx(node, pad);
    case 'blockColumns': return columnsToJsx(node, pad, indentLevel);
    case 'blockSocialIcons': return socialIconsToJsx(node, pad);
    default:             return null;
  }
}

function paragraphToJsx(node, pad) {
  const align = node.attrs?.textAlign || 'left';
  const htmlExpr = inlineToJsExpr(node.content || []);
  if (!htmlExpr) return null;
  return [
    `${pad}<Text`,
    `${pad}  style={{ textAlign: '${align}', color: '#676a6c', fontSize: '16px', lineHeight: '1.6', margin: '0 0 8px 0' }}`,
    `${pad}  dangerouslySetInnerHTML={{ __html: ${htmlExpr} }}`,
    `${pad}/>`,
  ].join('\n');
}

function headingToJsx(node, pad) {
  const level = node.attrs?.level || 2;
  const sizes   = { 1: '32px', 2: '24px', 3: '20px' };
  const weights = { 1: 700,    2: 700,    3: 600    };
  const htmlExpr = inlineToJsExpr(node.content || []);
  if (!htmlExpr) return null;
  return [
    `${pad}<Text`,
    `${pad}  style={{ fontSize: '${sizes[level] || '24px'}', fontWeight: ${weights[level] || 700}, lineHeight: '1.3', margin: '0 0 12px 0', color: '#333333' }}`,
    `${pad}  dangerouslySetInnerHTML={{ __html: ${htmlExpr} }}`,
    `${pad}/>`,
  ].join('\n');
}

function bulletListToJsx(node, pad) {
  const bullet = node.type === 'orderedList' ? null : '• ';
  const items = (node.content || []).map((item, idx) => {
    const paragraphs = (item.content || []).flatMap(p => {
      const html = buildInlineHtmlStr(p.content || []);
      const prefix = bullet ?? `${idx + 1}. `;
      return html ? [`${prefix}${html}`] : [];
    });
    return paragraphs.join('<br/>');
  }).filter(Boolean);

  if (items.length === 0) return null;
  const htmlExpr = wrapAsJsExpr(items.join('<br/>'));
  return [
    `${pad}<Text`,
    `${pad}  style={{ margin: '0 0 8px 0', paddingLeft: '16px', lineHeight: '1.6', fontSize: '16px', color: '#676a6c' }}`,
    `${pad}  dangerouslySetInnerHTML={{ __html: ${htmlExpr} }}`,
    `${pad}/>`,
  ].join('\n');
}

function imageToJsx(node, pad) {
  const { src = '', alt = '', width = '100%', align = 'center', href } = node.attrs || {};
  const w = typeof width === 'number' ? `${width}px` : width;
  const margin = align === 'center' ? '0 auto' : align === 'right' ? '0 0 0 auto' : '0';

  const imgLines = [
    `${pad}<Img`,
    `${pad}  src=${jsxAttrValue(src)}`,
    `${pad}  alt=${jsxAttrValue(alt)}`,
    `${pad}  width="${w}"`,
    `${pad}  style={{ display: 'block', margin: '${margin}' }}`,
    `${pad}/>`,
  ].join('\n');

  if (href) {
    return [
      `${pad}<Link href=${jsxAttrValue(href)}>`,
      imgLines.split('\n').map(l => '  ' + l).join('\n'),
      `${pad}</Link>`,
    ].join('\n');
  }
  return imgLines;
}

function buttonToJsx(node, pad) {
  const {
    label = 'Click here', href = '#',
    backgroundColor = '#D97706', color = '#ffffff',
    borderRadius = 6,
    paddingTop = 12, paddingBottom = 12, paddingLeft = 24, paddingRight = 24,
    fontSize = 14, fontWeight = 600,
  } = node.attrs || {};

  const labelContent = hasParamVars(label)
    ? substituteToJsxExpr(label)
    : escapeJsxText(label);

  return [
    `${pad}<Button`,
    `${pad}  href=${jsxAttrValue(href)}`,
    `${pad}  style={{`,
    `${pad}    backgroundColor: '${backgroundColor}',`,
    `${pad}    color: '${color}',`,
    `${pad}    borderRadius: '${borderRadius}px',`,
    `${pad}    padding: '${paddingTop}px ${paddingRight}px ${paddingBottom}px ${paddingLeft}px',`,
    `${pad}    fontSize: '${fontSize}px',`,
    `${pad}    fontWeight: ${fontWeight},`,
    `${pad}    textAlign: 'center',`,
    `${pad}    display: 'block',`,
    `${pad}  }}`,
    `${pad}>`,
    `${pad}  ${labelContent}`,
    `${pad}</Button>`,
  ].join('\n');
}

function dividerToJsx(node, pad) {
  const { color = '#E5E7EB', thickness = 1, width = 100 } = node.attrs || {};
  return [
    `${pad}<Hr`,
    `${pad}  style={{ borderColor: '${color}', borderTopWidth: '${thickness}px', width: '${width}%', margin: '16px auto' }}`,
    `${pad}/>`,
  ].join('\n');
}

function spacerToJsx(node, pad) {
  const { height = 24 } = node.attrs || {};
  return `${pad}<Section style={{ padding: '${height}px 0 0 0', margin: 0 }} />`;
}

function columnsToJsx(node, pad, indentLevel) {
  const { ratio = '50-50' } = node.attrs || {};
  const ratioMap = { '50-50': ['50%', '50%'], '33-66': ['33%', '66%'], '66-33': ['66%', '33%'] };
  const [lw, rw] = ratioMap[ratio] || ['50%', '50%'];
  const i1 = '  '.repeat(indentLevel + 1);
  const i2 = '  '.repeat(indentLevel + 2);

  const leftContent = (node.content?.[0]?.content || [])
    .map(n => nodeToJsx(n, indentLevel + 2)).filter(Boolean).join('\n');
  const rightContent = (node.content?.[1]?.content || [])
    .map(n => nodeToJsx(n, indentLevel + 2)).filter(Boolean).join('\n');

  return [
    `${pad}<Section>`,
    `${i1}<Column style={{ width: '${lw}', verticalAlign: 'top' }}>`,
    leftContent || `${i2}{/* empty */}`,
    `${i1}</Column>`,
    `${i1}<Column style={{ width: '${rw}', verticalAlign: 'top' }}>`,
    rightContent || `${i2}{/* empty */}`,
    `${i1}</Column>`,
    `${pad}</Section>`,
  ].join('\n');
}

function socialIconsToJsx(node, pad) {
  const { icons = [] } = node.attrs || {};
  if (icons.length === 0) return null;
  const i1 = pad + '  ';
  const i2 = i1 + '  ';

  const iconLines = icons.map(icon => [
    `${i1}<Link href=${jsxAttrValue(icon.href || '#')} style={{ display: 'inline-block', margin: '0 6px' }}>`,
    `${i2}<Img src=${jsxAttrValue(icon.iconUrl || '')} alt=${jsxAttrValue(icon.network || '')} width="24" height="24" />`,
    `${i1}</Link>`,
  ].join('\n')).join('\n');

  return [
    `${pad}<Row style={{ textAlign: 'center', margin: '8px 0' }}>`,
    iconLines,
    `${pad}</Row>`,
  ].join('\n');
}


// ─── Inline HTML string helpers ───────────────────────────────────────────────

/**
 * Build a JS expression (quoted string or template literal) from TipTap inline nodes.
 * Returns null if there is no meaningful content.
 */
function inlineToJsExpr(nodes) {
  if (!nodes || nodes.length === 0) return null;
  const html = buildInlineHtmlStr(nodes);
  if (!html.trim()) return null;
  return wrapAsJsExpr(html);
}

/** Build an HTML string from TipTap inline nodes, preserving {{ params.X }} tokens. */
function buildInlineHtmlStr(nodes) {
  if (!nodes) return '';
  return nodes.map(inlineNodeToHtmlStr).join('');
}

function inlineNodeToHtmlStr(node) {
  if (node.type === 'hardBreak') return '<br/>';
  if (node.type !== 'text') return '';

  let text = htmlEscapePreservingVars(node.text || '');

  if (node.marks) {
    for (const mark of node.marks) {
      switch (mark.type) {
        case 'bold':      text = `<strong>${text}</strong>`; break;
        case 'italic':    text = `<em>${text}</em>`; break;
        case 'underline': text = `<u>${text}</u>`; break;
        case 'code':      text = `<code>${text}</code>`; break;
        case 'link': {
          const href = mark.attrs?.href || '#';
          // preserve variable tokens in href
          const hrefStr = hasParamVars(href)
            ? href.replace(/\{\{\s*params\.(\w+)\s*\}\}/g, '${$1}')
            : href;
          if (hasParamVars(href)) {
            text = `<a href="\${${href.match(/params\.(\w+)/)?.[1] || 'url'}}">${text}</a>`;
          } else {
            text = `<a href="${href}">${text}</a>`;
          }
          break;
        }
        case 'textStyle':
          if (mark.attrs?.color) text = `<span style="color:${mark.attrs.color}">${text}</span>`;
          break;
      }
    }
  }
  return text;
}

/** HTML-escape a string while preserving {{ params.X }} tokens unchanged. */
function htmlEscapePreservingVars(str) {
  const parts = str.split(/(\{\{\s*params\.\w+\s*\}\})/g);
  return parts.map((part, i) => {
    if (i % 2 === 1) return part; // variable token — keep as-is
    return part.replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;');
  }).join('');
}

/**
 * Wrap an HTML string as a JS expression for dangerouslySetInnerHTML.
 * If it contains {{ params.X }}, uses a template literal with ${X} substitution.
 * Otherwise returns a double-quoted string.
 */
function wrapAsJsExpr(htmlStr) {
  if (!hasParamVars(htmlStr)) {
    const escaped = htmlStr.replace(/\\/g, '\\\\').replace(/"/g, '\\"').replace(/\n/g, '\\n');
    return `"${escaped}"`;
  }
  const withSubs = substituteParamVars(htmlStr);
  const escaped = withSubs.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\n/g, '\\n');
  return `\`${escaped}\``;
}

/** JSX attribute value: "static", {varName}, or {`tem${plate}`} */
function jsxAttrValue(str) {
  if (!str) return '""';
  if (!hasParamVars(str)) {
    return `"${str.replace(/\\/g, '\\\\').replace(/"/g, '\\"')}"`;
  }
  const pure = str.match(/^\{\{\s*params\.(\w+)\s*\}\}$/);
  if (pure) return `{${pure[1]}}`;
  const withSubs = substituteParamVars(str).replace(/`/g, '\\`');
  return `{\`${withSubs}\`}`;
}

/** Convert a button label string to JSX children content. */
function substituteToJsxExpr(str) {
  const pure = str.match(/^\{\{\s*params\.(\w+)\s*\}\}$/);
  if (pure) return `{${pure[1]}}`;
  const withSubs = substituteParamVars(str).replace(/`/g, '\\`');
  return `{\`${withSubs}\`}`;
}

/** Escape a static string for use as JSX text content. */
function escapeJsxText(str) {
  return str.replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;');
}

function hasParamVars(str) {
  return /\{\{\s*params\.\w+\s*\}\}/.test(str);
}

function substituteParamVars(str) {
  return str.replace(/\{\{\s*params\.(\w+)\s*\}\}/g, '$$$1}').replace(/\$(\w+)\}/g, '${$1}');
}


// ─── Naming utilities ─────────────────────────────────────────────────────────

function toSlug(name) {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
}

function toPascalCase(slug) {
  return slug.split('-').map(s => s.charAt(0).toUpperCase() + s.slice(1)).join('');
}
